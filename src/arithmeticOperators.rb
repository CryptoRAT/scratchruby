# Let's look at some arithmetic operators and results in ruby.
puts '6 + 4  = ' + (6+4).to_s
puts '6 - 4  = ' + (6-4).to_s
puts '6 * 4  = ' + (6*4).to_s
puts '6 / 4  = ' + (6/4).to_s
puts '6 % 4  = ' + (6%4).to_s

puts "Now let's swap the digits."
puts '4 + 6 = ' + (4+6).to_s
puts '4 - 6 = ' + (4-6).to_s
puts '4 * 6 = ' + (4*6).to_s
puts '4 / 6 = ' + (4/6).to_s
puts '4 % 6 = ' + (4%6).to_s
